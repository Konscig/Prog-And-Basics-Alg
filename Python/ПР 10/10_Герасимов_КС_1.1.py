#a - действительное число
#n - натуральное число

#k = 1 - циклкическая переменная
#z = -1
#a = a*a вычисление а^2
#f = 1 - факториал
#s = 0 summa

#s - выходной параметр, сумма

a = float(input())
n = int(input())
if n >= 1:
    z = -1
    f = 1
    a2 = a*a
    s = 0
    for k in range(1, n+1):
        z *= -1
        a2 *= a
        f = 3*k  * (3*k-1) * (3*k-2)
        s += z*a2/f
print(s)
